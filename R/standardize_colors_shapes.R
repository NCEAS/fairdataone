#These are standardized shapes and colors for figure generation
#

standardize_colors_shapes <- function() {

    shapeValues <- c("INITIAL" = 22, "INTERMEDIATE"=21, "FINAL"=21, "DOI"=21)
    sizeValues <- c("INITIAL" = 3, "INTERMEDIATE"=2.5, "FINAL"=2.5, "DOI"=2.5)
    fillValues <- c("INITIAL" = "gray90", "INTERMEDIATE" = "gray70", "FINAL" = "black", "DOI"= "#19B36A")
    colorValues <- c("INITIAL" = "black",  "INTERMEDIATE" = "gray80", "FINAL" = "black", "DOI" = "#1D254E")

    return(shapeValues, sizeValues, fillValues, colorValues)

}
